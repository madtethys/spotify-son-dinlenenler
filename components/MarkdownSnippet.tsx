import React, { useState, useCallback, useEffect } from 'react';
import Icon from '@ant-design/icons';
import { Input, Space, Typography, Tabs, Slider, Switch, Button, Select, message, Radio, RadioChangeEvent } from 'antd';
import * as Constants from '../utils/Constants';
import InstagramIcon from './InstagramIcon';

const { Text, Title } = Typography;
const { TextArea } = Input;
const { TabPane } = Tabs;
const { Option } = Select;

interface Props {
    username?: string;
    theme: string;
}

export default function MarkdownSnippet(props: Props): JSX.Element | null {
    const { username, theme } = props;
    const [trackCount, setTrackCount] = useState<number>(5);
    const [width, setWidth] = useState<number>(400);
    const [uniqueTracks, setUniqueTracks] = useState<boolean>(false);
    const [selectedBackground, setSelectedBackground] = useState<string>('https://spotify.mdusova.com/arkaplan1.png');
    const [loading, setLoading] = useState<boolean>(false);
    const [dynamicSvgSrc, setDynamicSvgSrc] = useState<string>(`${Constants.BaseUrl}/api?user=${username}`);

    if (!username) {
        return null;
    }

    // Varsayƒ±lan URL, parametreler eklenmeden
    let svgSrc = `${Constants.BaseUrl}/api?user=${username}`;

useEffect(() => {
    let newSvgSrc = `${Constants.BaseUrl}/api?user=${username}`;

    if (trackCount !== 5) {
        newSvgSrc += `&count=${trackCount}`;
    }
    if (width !== 400) {
        newSvgSrc += `&width=${width}`;
    }
    if (uniqueTracks) {
        newSvgSrc += `&unique=${uniqueTracks}`;
    }

    setDynamicSvgSrc(newSvgSrc);
}, [trackCount, width, uniqueTracks, username]);

    const backgrounds = [
        'https://spotify.mdusova.com/arkaplan1.png',
        'https://spotify.mdusova.com/arkaplan2.png',
        'https://spotify.mdusova.com/arkaplan3.png',
        'https://spotify.mdusova.com/arkaplan4.png',
        'https://spotify.mdusova.com/arkaplan5.png',
        'https://spotify.mdusova.com/arkaplan6.png',
        'https://spotify.mdusova.com/arkaplan7.png',
        'https://spotify.mdusova.com/arkaplan8.png',
    ];

const handleBackgroundSelect = useCallback((e: RadioChangeEvent) => {
    setSelectedBackground(e.target.value);
}, []);

const svgToPng = async (svgUrl: string) => {
    const response = await fetch(svgUrl);
    const svgText = await response.text();

    // UTF-8 ile Base64 kodlama
    const base64 = btoa(unescape(encodeURIComponent(svgText)));

    const canvas = document.createElement('canvas');
    const img = new Image();
    img.src = 'data:image/svg+xml;base64,' + base64;
    await new Promise((resolve) => {
        img.onload = () => resolve(null);
    });
    canvas.width = img.width;
    canvas.height = img.height;
    const ctx = canvas.getContext('2d');
    ctx?.drawImage(img, 0, 0);
    return canvas.toDataURL('image/png');
};

const mergeImageWithBackground = async (apiImage: string, backgroundImage: string) => {
    const canvas = document.createElement('canvas');
    const ctx = canvas.getContext('2d');

    const bgImg = await loadImage(backgroundImage);
    const apiImg = await loadImage(apiImage);

    canvas.width = bgImg.width;
    canvas.height = bgImg.height;

    ctx?.drawImage(bgImg, 0, 0);

    const padding = 20; // Bo≈üluk ayarƒ±
    const scaleFactor = 2.5; // G√∂rseli b√ºy√ºtmek i√ßin √∂l√ßek fakt√∂r√º
    const imgWidth = apiImg.width * scaleFactor;
    const imgHeight = apiImg.height * scaleFactor;

    const imgX = (canvas.width - imgWidth) / 2;
    const imgY = (canvas.height - imgHeight) / 2;

    ctx?.drawImage(apiImg, imgX + padding, imgY, imgWidth - padding * 2, imgHeight);

    return canvas.toDataURL('image/png');
};


    const loadImage = (src: string) => {
        return new Promise<HTMLImageElement>((resolve, reject) => {
            const img = new Image();
            img.crossOrigin = 'use-credentials';
            img.onload = () => resolve(img);
            img.onerror = reject;
            img.src = src;
        });
    };

    const handleShareClick = async () => {
        setLoading(true);
        try {
            const pngImage = await svgToPng(svgSrc);
            const finalImage = await mergeImageWithBackground(pngImage, selectedBackground);
            const link = document.createElement('a');
            link.href = finalImage;
            link.download = `spotify_son_dinlenenler_${username}.png`;
            link.click();
            message.success('Resim indirildi, Instagram hikayenize y√ºkleyebilirsiniz!');
        } catch (error) {
            console.error("Birle≈ütirme hatasƒ±:", error);
            message.error('Bir hata olu≈ütu.');
        } finally {
            setLoading(false);
        }
    };

        const handleWidthChange = useCallback((value: number | [number, number]) => {
        if (typeof value === 'number') {
            setWidth(value);
        }
    }, []);

    const handleTrackCountChange = useCallback((value: number | [number, number]) => {
        if (typeof value === 'number') {
            setTrackCount(value);
        }
    }, []);

    return (
        <div>
            <Title level={5} style={{ color: theme === 'dark' ? '#e0e0e0' : '#222222', marginBottom: '20px' }}>
                üë§ "{username}" olarak giri≈ü yapƒ±ldƒ±.
            </Title>
            <Tabs defaultActiveKey="1">
                <TabPane tab="üåê Websiteme Nasƒ±l Eklerim?" key="1">
                    <Space className="vert-space" direction="vertical" size="small">
                        <Title level={5} style={{ color: theme === 'dark' ? '#ffffff' : '#222222' }}>
                            HTML'e eklemek i√ßin kodunuz:
                        </Title>
                        <Text style={{ color: theme === 'dark' ? '#e0e0e0' : '#434242', fontSize: '14px' }}>
                            ‚ÑπÔ∏è L√ºtfen bu kodu HTML kodunuzda eklemek istediƒüiniz yere ekleyin.
                        </Text>
                        <TextArea
                            className="htmlkodu"
                            autoSize
                            readOnly
                            value={`<img src="${dynamicSvgSrc}" alt="Spotify Son Dinlenen M√ºzikler - ${username}" />`}
                            style={{
                                backgroundColor: theme === 'dark' ? '#333333' : '#ffffff',
                                color: theme === 'dark' ? '#e0e0e0' : '#222222'
                            }}
                        />
                        <Title level={5} style={{ color: theme === 'dark' ? '#ffffff' : '#222222' }}>
                            Markdown'a eklemek i√ßin kodunuz:
                        </Title>
                        <Text style={{ color: theme === 'dark' ? '#e0e0e0' : '#434242', fontSize: '14px' }}>
                            ‚ÑπÔ∏è L√ºtfen bu kodu markdown dosyanƒ±zda eklemek istediƒüiniz yere ekleyin.
                        </Text>
                        <TextArea
                            className="markdown"
                            autoSize
                            readOnly
                            value={`![Spotify Son Dinlenen M√ºzikler](${dynamicSvgSrc})`}
                            style={{
                                backgroundColor: theme === 'dark' ? '#333333' : '#ffffff',
                                color: theme === 'dark' ? '#e0e0e0' : '#222222'
                            }}
                        />

                        <Title level={5} style={{ color: theme === 'dark' ? '#ffffff' : '#222222' }}>
                            √ñnizleme:
                        </Title>
                        <img src={dynamicSvgSrc} alt="Spotify Son Dinlenen M√ºzikler √ñnizleme" style={{ width: '100%', maxWidth: `${width}px` }} />
                    </Space>
                </TabPane>

                 <TabPane tab="üé® Instagram Hikayesinde Payla≈ü" key="2">
                    <Space className="vert-space" direction="vertical" size="small">
                        <Title level={5} style={{ color: theme === 'dark' ? '#ffffff' : '#222222' }}>
                            üé® Arka Plan Se√ßimi:
                        </Title>
                        <Text style={{ color: theme === 'dark' ? '#e0e0e0' : '#434242', fontSize: '14px' }}>
                            ‚ÑπÔ∏è L√ºtfen arka planda kullanacaƒüƒ±nƒ±z g√∂r√ºnt√ºy√º se√ßiniz.
                        </Text>
                        <div style={{ display: 'flex', flexWrap: 'wrap', gap: '10px' }}>
                            {backgrounds.map((background, index) => (
                                <div key={index} style={{ width: 'calc(50% - 5px)' }}>
                                    <Radio
                                        value={background}
                                        style={{
                                            display: 'flex',
                                            alignItems: 'center',
                                            padding: '10px',
                                            backgroundColor: theme === 'dark' ? '#555' : '#f5f5f5',
                                            borderRadius: '8px',
                                            cursor: 'pointer',
                                            width: '100%',
                                        }}
                                        checked={selectedBackground === background}
                                        onChange={handleBackgroundSelect}
                                    >
                                        <img
                                            src={background}
                                            alt={`Arka Plan ${index + 1}`}
                                            style={{
                                                width: '60px',
                                                height: '60px',
                                                marginRight: '10px',
                                                borderRadius: '8px',
                                                objectFit: 'cover',
                                            }}
                                        />
                                        Arka Plan {index + 1}
                                    </Radio>
                                </div>
                            ))}
                        </div>


                        <Title level={5} style={{ color: theme === 'dark' ? '#ffffff' : '#222222' }}>
                            üì§ Instagram Hikayesi i√ßin Payla≈ü:
                        </Title>
                        <Text style={{ color: theme === 'dark' ? '#e0e0e0' : '#434242', fontSize: '14px' }}>
                            ‚ÑπÔ∏è A≈üaƒüƒ±daki butona basarak g√∂rseli indirebilir ve ardƒ±ndan Instagram hikayenizde payla≈üabilirsiniz.
                        </Text>
                        <Button
                          className="instagram-share-btn"
                          shape="round"
                          icon={<Icon component={InstagramIcon} />}
                          size="large"
                          type="primary"
                          onClick={handleShareClick}
                          loading={loading}
                        >
                        Instagram Hikayende Payla≈ü
                        </Button>

                    </Space>
                </TabPane>

                <TabPane tab="‚öôÔ∏è Ayarlarƒ± Yapƒ±landƒ±rƒ±n" key="3">
                    <Space direction="vertical" size="large" style={{ marginBottom: 16 }}>
                        <div>
                            <Title level={5} style={{ color: theme === 'dark' ? '#ffffff' : '#222222' }}>
                                üéµ Listedeki M√ºzik Sayƒ±sƒ±:
                            </Title>
                            <Text style={{ color: theme === 'dark' ? '#e0e0e0' : '#434242', fontSize: '14px' }}>
                              ‚ÑπÔ∏è Listede bulunan m√ºzik sayƒ±sƒ±nƒ± bu ayar ile ayarlayabilirsiniz. <br /> Minimum deƒüer: 1 / Maksimum deƒüer: 10 (Varsayƒ±lan deƒüer: 5)
                            </Text>
                            <Slider
                                defaultValue={trackCount}
                                min={1}
                                max={10}
                                onChange={handleTrackCountChange}
                                value={trackCount}
                                style={{ maxWidth: '600px' }}
                            />
                        </div>

                        <div>
                            <Title level={5} style={{ color: theme === 'dark' ? '#ffffff' : '#222222' }}>
                                üìê Liste Geni≈üliƒüi(px):
                            </Title>
                            <Text style={{ color: theme === 'dark' ? '#e0e0e0' : '#434242', fontSize: '14px' }}>
                             ‚ÑπÔ∏è Listenizin geni≈üliƒüini bu ayar ile ayarlayabilirsiniz. <br /> Minimum deƒüer: 300 / Maksimum deƒüer: 1000 (Varsayƒ±lan deƒüer: 400)
                            </Text>
                            <Slider
                                defaultValue={width}
                                min={300}
                                max={1000}
                                onChange={handleWidthChange}
                                value={width}
                                style={{ maxWidth: '600px' }}
                            />
                        </div>

                        <div>
                            <Title level={5} style={{ color: theme === 'dark' ? '#ffffff' : '#222222' }}>
                                üéß Tekrarlanan M√ºzikler:
                            </Title>
                            <Text style={{ color: theme === 'dark' ? '#e0e0e0' : '#434242', fontSize: '14px' }}>
                            ‚ÑπÔ∏è Listede tekrar dinlediƒüiniz m√ºzikleri bu ayar ile g√∂sterebilirsiniz.
                            </Text>
                            <Switch
                                checked={uniqueTracks}
                                onChange={setUniqueTracks}
                                checkedChildren="G√∂ster"
                                unCheckedChildren="G√∂sterme"
                            />
                        </div>
                    </Space>
                </TabPane>
            </Tabs>
        </div>
    );
}
